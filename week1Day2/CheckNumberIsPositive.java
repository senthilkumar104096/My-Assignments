package week1Day2;

public class CheckNumberIsPositive {

	public static void main(String[] args) {
		//Initialize the variable
		int num = -2000;
		
		// using if condition
	    if (num>=0) {
	    	System.out.println(num + " - is a Postive Number");
	    }else {
	    	System.out.println(num + " - is a Negative Number");
	}

}
}