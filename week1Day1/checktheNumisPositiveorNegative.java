package week1Day1;

public class checktheNumisPositiveorNegative {

	public static void main(String[] args) {
		//Initialize the variable
				int num = 2000;
				// using nested if else condition
			    if (num>0) {
			    	System.out.println(num + " - number is a postive value");
			    }else if (num<0) {
			    	System.out.println(num + " - number is a negative value");
			    }else {
			    	System.out.println(num + " - number is a zero value");
			    }

	}

}
